#include <stdio.h>

int main() {
    int num1, num2;

    // Taking input
  
    scanf("%d", &num1);

   
    scanf("%d", &num2);

    // Checking if the first number is greater than the second
    if (num1 > num2) {
        printf("True");
    } else {
        printf("False");
    }

    return 0;
}
